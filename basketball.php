    <label class="rules">Rules of Basketball: </label>
    <p class="basket">
        Basketball is a game played between two teams, each consisting of five players.

        The objective of the game is to score points by shooting the basketball through the opponent's hoop and preventing

        the opponent from scoring by defending their hoop. The game starts with a jump ball, where the referee throws

        the ball into the air between two players, and the player who catches it first gains possession for their team.

        The player in possession of the ball must dribble it by bouncing it on the floor while moving or stationary.

        Passing is also allowed, but players cannot throw the ball out of bounds or to an opponent intentionally.

        Most organized basketball games have a shot clock, which is a timer that limits the time a team has to attempt a shot.

        Scoring is awarded for successful shots made from different locations on the court. Rebounding occurs when a shot is missed,

        and players try to grab the rebound to gain possession of the ball. Fouls are committed if players engage in illegal physical

        contact with opponents, and the opposing team is awarded free throws or possession of the ball. Teams are allowed to substitute

        players during certain stoppages of play, such as timeouts. Overtime is played in case of a tie at the end of regulation

        time to determine the winner. There are various violations in basketball, such as traveling and double dribble.

        Jump ball situations occur when two players from opposing teams gain possession of the ball simultaneously

        or when the ball becomes lodged between the hoop and the backboard. These are some of the basic rules of basketball.

        However, it's important to note that different leagues and organizations may have slightly different rules and regulations,

        so it's always best to consult the specific rulebook for the league or organization you are playing in or watching.
